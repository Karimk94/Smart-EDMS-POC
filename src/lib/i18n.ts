export const dictionaries: Record<Lang, Dictionary> = { en, ar };

export function translate(lang: Lang, key: string, replacements?: Record<string, string | number>) {
  let value = dictionaries[lang][key] || dictionaries.en[key] || key;
  if (replacements) {
    for (const [name, replacement] of Object.entries(replacements)) {
      value = value.replaceAll(`{${name}}`, String(replacement));
    }
  }
  return value;
}
